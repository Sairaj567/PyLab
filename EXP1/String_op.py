mystring = "Hello world"

#converting to capital
print("UPPER CASE: ",mystring.upper())

#converting to lower
print("lower case: ",mystring.lower())

#Replaing a char
print("Replacing H with J: ",mystring.replace('H','J'))

#slice
print("First 5 letter: ",mystring[0:5])

#indexing
print("Index 6: ",mystring[6])

#replace string
print("Replacing \"world\" with \"Python\": ",mystring.replace("world","Python"))


